class Solution {
public:
    string reverseVowels(string s) {
        string a;
        for (int i = 0; i < s.size(); i++)
        {
            if(s[i]=='a'||s[i]=='e'||s[i]=='i'||s[i]=='o'||s[i]=='u'||s[i]=='A'||s[i]=='E'||s[i]=='I'||s[i]=='O'||s[i]=='U')a+=s[i];
        }
        reverse(all(a));
        int j=0;
        for (int i = 0; i < s.size(); i++)
        {
            if(s[i]=='a'||s[i]=='e'||s[i]=='i'||s[i]=='o'||s[i]=='u'||s[i]=='A'||s[i]=='E'||s[i]=='I'||s[i]=='O'||s[i]=='U')s[i]=a[j++];
        }
        return s;
    }
};