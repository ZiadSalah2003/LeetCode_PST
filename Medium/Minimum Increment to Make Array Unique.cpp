const double EPS = 1e-7;
class Solution {
public:
    int minIncrementForUnique(vector<int>& nums) {
        sort(all(nums));
        int ans = 0;
        for (int i = 1; i < sz(nums); i++) {
            if (nums[i] <= nums[i - 1]) {
                ans += nums[i - 1] - nums[i] + 1;
                nums[i] = nums[i - 1] + 1;
            }
        }
        return ans;
    }
};